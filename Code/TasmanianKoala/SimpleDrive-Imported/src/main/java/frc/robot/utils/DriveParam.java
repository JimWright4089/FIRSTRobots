package frc.robot.utils;

//----------------------------------------------------------------------------
//Class Declarations
//----------------------------------------------------------------------------
//
// Class Name: DriveParam
//
// Purpose:
//  
//
//----------------------------------------------------------------------------
public class DriveParam  {
  //----------------------------------------------------------------------------
  //  Class Atributes
  //----------------------------------------------------------------------------
  private double mForward = 0.0;
  private double mRotation = 0.0;

  // --------------------------------------------------------------------
  // Purpose:
  //   Constructors
  //
  // Notes:
  //   None.
  // --------------------------------------------------------------------
  public DriveParam() {
  }

  // --------------------------------------------------------------------
  // Purpose:
  //   Constructors
  //
  // Notes:
  //   None.
  // --------------------------------------------------------------------
  public DriveParam(double forward, double rotation) {
    mForward = forward;
    mRotation = rotation;
  }

  // --------------------------------------------------------------------
  // Purpose:
  //   return forward
  //
  // Notes:
  //   None.
  // --------------------------------------------------------------------
  public double getForward() {
    return mForward;
  }

  // --------------------------------------------------------------------
  // Purpose:
  //   return rotation
  //
  // Notes:
  //   None.
  // --------------------------------------------------------------------
  public double getRotation() {
    return mRotation;
  }

  // --------------------------------------------------------------------
  // Purpose:
  //   Set the forward
  //
  // Notes:
  //   None.
  // --------------------------------------------------------------------
  public void setForward(double value) {
    mForward = value;
  }

  // --------------------------------------------------------------------
  // Purpose:
  //   Set the rotation
  //
  // Notes:
  //   None.
  // --------------------------------------------------------------------
  public void setRotation(double value) {
    mRotation = value;
  }

  // --------------------------------------------------------------------
  // Purpose:
  //   Multiply the values
  //
  // Notes:
  // None.
  // --------------------------------------------------------------------
  public void multiply(double forward, double turn)
  {
      mForward *= forward;
      mRotation *= turn;
  }

  // --------------------------------------------------------------------
  // Purpose:
  //   clear the values
  //
  // Notes:
  // None.
  // --------------------------------------------------------------------
  public void clear()
  {
    mForward = 0.0;
    mRotation = 0.0;
  }

}